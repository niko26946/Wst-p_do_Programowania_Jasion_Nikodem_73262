import math
import cmath  # do pierwiastków z liczb ujemnych

# a) √81
a = math.sqrt(81)
print("a) √81 =", a)

# b) 8^10
b = 8 ** 10
print("b) 8^10 =", b)

# c) √2 + √3 + √6
c = math.sqrt(2) + math.sqrt(3) + math.sqrt(6)
print("c) √2 + √3 + √6 =", c)

# d) √(-5)
d = cmath.sqrt(-5)
print("d) √(-5) =", d)

# e) ∛125
e = 125 ** (1/3)
print("e) ∛125 =", e)
